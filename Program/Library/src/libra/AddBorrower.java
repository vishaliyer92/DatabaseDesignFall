    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        String FNAME = fname.getText();
        String LNAME = lname.getText();
        String ADDRESS = addr.getText();
        String CITY = city.getText();
        String STATE = state.getText();
        String PHONE = phone.getText();
        if(("".equals(FNAME))|("".equals(LNAME))|("".equals(ADDRESS))|("".equals(CITY))|("".equals(STATE))|("".equals(PHONE)))
        {
           JOptionPane.showMessageDialog(null, "Please enter all the information");
        }
        
        else{
            try 
            {   
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","romi");
                              
                int length = PHONE.length();
                Statement st = con.createStatement();
                if(length == 10)
                {
                String geck = "select count(*) as c from borrower where Fname='"+FNAME+"' and Lname='"+LNAME+"'and Address='"+ADDRESS+ "' and city='"+CITY+"'and State='"+STATE+"'";
                ResultSet rs=st.executeQuery(geck);
                int count=0;
                while(rs.next())
                {
                 count=Integer.parseInt(rs.getString("c"));
                }
                if(count==0)
                {
                 String query="insert into borrower (fname,lname,address,city,state,phone) "
                    + "values('"+FNAME+"','"
                    +LNAME+"','"
                    +ADDRESS+"','"
                    +CITY+"','"
                    +STATE+"','"    
                    +PHONE+"')";    
                
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(null, "User Created!");
                }
                else
                {
                   JOptionPane.showMessageDialog(null,"Error: There is an existing borrower!"); 
                }
                
                
                
                }
                else{
                JOptionPane.showMessageDialog(null, "Phone number should be 10 digits");
                }
                
                
            } 
            catch (SQLException ex)
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
                //ex.printStackTrace();
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddBorrower.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
      
        
    }//GEN-LAST:event_addbtnActionPerformed

